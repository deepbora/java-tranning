
package mycompany;

import java.util.Scanner;


public class Elseif {


    public static void main(String[] args) {
        
        
        int x;
        
       Scanner sc = new Scanner(System.in);
        System.out.println("Enter the number :");
        x =sc.nextInt();
        
        
        
        if(x==1)
            System.out.println("RED");
        
        else if(x==2)
            System.out.println("GREEN");
        else if(x==3)
            System.out.println("BLUE");
        
        
        
        
        
        
        
    }
    
}
