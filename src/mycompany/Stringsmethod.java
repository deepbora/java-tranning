package mycompany;

public class Stringsmethod {

    public static void main(String[] args) {

        String ch = "deep ";
        String c = "Deep";

        System.out.println(ch.toUpperCase());
        System.out.println(ch.replace(ch, ch));
        System.out.println(ch.trim());
        System.out.println(ch.equals(c));
        System.out.println(ch.equalsIgnoreCase(c));
        System.out.println(ch.length());
        System.out.println(ch.concat(c));
        System.out.println(ch.concat(c));
        System.out.println(ch.substring(0));
        System.out.println(ch.concat(c));

    }

}
